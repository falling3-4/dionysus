<h1>WELCOME TO DIONYSUS</h1>
<hr />
<p>
	I made this website with my bare two hands. No generative ai was used in the making of this
	website.
	<br />
	<br />
	Here you can play and browse unrestricted wherever you are, being school or North Korea. I hope you
	enjoy your stay here. It took a lot of work to get this website up and running, and I hope you enjoy
	it. If you have any suggestions or feedback, please let me know.
	<br />
	<br />
	This site was inspired by the old internet and web design of the early 2000s. I've always thought this
	type of web design feels unworldly, so I used it to tie into the god theming (Dionysus being the god
	of "fun" basically, thats why the site is designed to be fun and enjoyable to use). I also wanted to
	create a website that was simple and easy to use, while still being visually appealing. I hope I succeeded
	in that regard.
</p>
